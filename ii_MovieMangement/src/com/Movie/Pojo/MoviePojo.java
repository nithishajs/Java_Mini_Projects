package com.Movie.Pojo;
/*
 * In we use in the class 
 * Encapsulation
 * Private modifier
 */

public class MoviePojo {
	
	private int movieId;
	private String movieName;
	private String movieText;
	private String movieCategory;
	private String assignMovieTo;
	
	public int getMovieId() {
		return movieId;
	}
	public void setMovieId(int movieId) {
		this.movieId = movieId;
	}
	public String getMovieName() {
		return movieName;
	}
	public void setMovieName(String movieName) {
		this.movieName = movieName;
	}
	public String getMovieText() {
		return movieText;
	}
	public void setMovieText(String movieText) {
		this.movieText = movieText;
	}
	public String getMovieCategory() {
		return movieCategory;
	}
	public void setMovieCategory(String movieCategory) {
		this.movieCategory = movieCategory;
	}
	public String getAssignMovieTo() {
		return assignMovieTo;
	}
	public void setAssignMovieTo(String assignMovieTo) {
		this.assignMovieTo = assignMovieTo;
	}
	
	

}
